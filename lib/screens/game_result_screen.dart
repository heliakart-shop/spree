import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import '../theme/app_theme.dart';
import '../widgets/common_widgets.dart';
import '../models/game_data.dart';
import '../utils/app_utils.dart';
import 'game_settings_screen.dart';

class GameResultScreen extends StatefulWidget {
  final GameData gameData;

  const GameResultScreen({
    super.key,
    required this.gameData,
  });

  @override
  State<GameResultScreen> createState() => _GameResultScreenState();
}

class _GameResultScreenState extends State<GameResultScreen>
    with TickerProviderStateMixin {
  late AnimationController _fadeController;
  late AnimationController _slideController;
  late Animation<double> _fadeAnimation;
  late Animation<Offset> _slideAnimation;

  @override
  void initState() {
    super.initState();

    _fadeController = AnimationController(
      duration: AppAnimations.normal,
      vsync: this,
    );

    _slideController = AnimationController(
      duration: AppAnimations.slow,
      vsync: this,
    );

    _fadeAnimation = Tween<double>(
      begin: 0.0,
      end: 1.0,
    ).animate(CurvedAnimation(
      parent: _fadeController,
      curve: AppAnimations.easeIn,
    ));

    _slideAnimation = Tween<Offset>(
      begin: const Offset(0, 0.1),
      end: Offset.zero,
    ).animate(CurvedAnimation(
      parent: _slideController,
      curve: AppAnimations.easeOut,
    ));

    _fadeController.forward();
    _slideController.forward();
  }

  @override
  void dispose() {
    _fadeController.dispose();
    _slideController.dispose();
    super.dispose();
  }

  void _copyGameToClipboard() {
    final gameText = _buildGameText();
    Clipboard.setData(ClipboardData(text: gameText));
    
    AppUtils.showSnackBar(
      context,
      'Game copied to clipboard!',
      backgroundColor: AppColors.primary.withOpacity(0.9),
      textColor: AppColors.onPrimary,
    );
    
    AppUtils.lightHaptic();
  }

  String _buildGameText() {
    final buffer = StringBuffer();
    buffer.writeln('üéâ ${widget.gameData.title}');
    buffer.writeln();
    buffer.writeln('üìù Description:');
    buffer.writeln(widget.gameData.description);
    buffer.writeln();
    buffer.writeln('‚öôÔ∏è Game Details:');
    buffer.writeln('‚Ä¢ Players: ${widget.gameData.playerCount}');
    buffer.writeln('‚Ä¢ Duration: ${widget.gameData.duration} minutes');
    buffer.writeln('‚Ä¢ Difficulty: ${widget.gameData.difficulty}');
    buffer.writeln();
    buffer.writeln('üìã Rules:');
    for (int i = 0; i < widget.gameData.rules.length; i++) {
      buffer.writeln('${i + 1}. ${widget.gameData.rules[i]}');
    }
    buffer.writeln();
    buffer.writeln('üéØ Instructions:');
    buffer.writeln(widget.gameData.instructions);
    buffer.writeln();
    buffer.writeln('üèÜ Scoring:');
    buffer.writeln(widget.gameData.scoringSystem);
    buffer.writeln();
    if (widget.gameData.materials.isNotEmpty) {
      buffer.writeln('üõ† Materials Needed:');
      for (final material in widget.gameData.materials) {
        buffer.writeln('‚Ä¢ $material');
      }
      buffer.writeln();
    }
    if (widget.gameData.tips.isNotEmpty) {
      buffer.writeln('üí° Tips:');
      for (final tip in widget.gameData.tips) {
        buffer.writeln('‚Ä¢ $tip');
      }
    }
    buffer.writeln();
    buffer.writeln('Generated by Spree Game AI ü§ñ');

    return buffer.toString();
  }

  void _generateNewGame() {
    Navigator.pushReplacement(
      context,
      AppAnimations.fadeTransition(
        page: GameSettingsScreen(gameType: widget.gameData.gameType),
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: SafeArea(
        child: FadeTransition(
          opacity: _fadeAnimation,
          child: SlideTransition(
            position: _slideAnimation,
            child: Column(
              children: [
                // Header
                _buildHeader(),
                
                // Game content
                Expanded(
                  child: SingleChildScrollView(
                    padding: const EdgeInsets.symmetric(horizontal: 24.0),
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        _buildGameTitle(),
                        const SizedBox(height: 24),
                        _buildGameInfo(),
                        const SizedBox(height: 24),
                        _buildGameDescription(),
                        const SizedBox(height: 24),
                        _buildRulesSection(),
                        const SizedBox(height: 24),
                        _buildInstructionsSection(),
                        const SizedBox(height: 24),
                        _buildScoringSection(),
                        const SizedBox(height: 24),
                        if (widget.gameData.materials.isNotEmpty) ...[
                          _buildMaterialsSection(),
                          const SizedBox(height: 24),
                        ],
                        if (widget.gameData.tips.isNotEmpty) ...[
                          _buildTipsSection(),
                          const SizedBox(height: 24),
                        ],
                      ],
                    ),
                  ),
                ),
                
                // Bottom actions
                _buildBottomActions(),
              ],
            ),
          ),
        ),
      ),
    );
  }

  Widget _buildHeader() {
    return Padding(
      padding: const EdgeInsets.all(24.0),
      child: Row(
        children: [
          IconButton(
            onPressed: () => Navigator.pop(context),
            icon: const Icon(
              Icons.arrow_back_ios,
              color: AppColors.lightBeige,
            ),
          ),
          const SizedBox(width: 8),
          Expanded(
            child: Text(
              'Your Perfect Game!',
              style: Theme.of(context).textTheme.headlineMedium?.copyWith(
                fontSize: 20,
              ),
            ),
          ),
          IconButton(
            onPressed: _copyGameToClipboard,
            icon: const Icon(
              Icons.copy,
              color: AppColors.lightBeige,
            ),
            tooltip: 'Copy game',
          ),
        ],
      ),
    );
  }

  Widget _buildGameTitle() {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Container(
          padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 6),
          decoration: BoxDecoration(
            color: AppColors.primary.withOpacity(0.2),
            borderRadius: BorderRadius.circular(16),
            border: Border.all(
              color: AppColors.primary.withOpacity(0.3),
            ),
          ),
          child: Text(
            widget.gameData.gameType,
            style: Theme.of(context).textTheme.bodyMedium?.copyWith(
              color: AppColors.primary,
              fontWeight: FontWeight.w600,
            ),
          ),
        ),
        const SizedBox(height: 16),
        Text(
          widget.gameData.title,
          style: Theme.of(context).textTheme.headlineLarge?.copyWith(
            fontSize: 32,
            height: 1.2,
          ),
        ),
      ],
    );
  }

  Widget _buildGameInfo() {
    return GlassContainer(
      color: AppColors.lightBeige,
      padding: const EdgeInsets.all(20),
      child: Row(
        children: [
          Expanded(
            child: _buildInfoItem(
              Icons.group,
              'Players',
              widget.gameData.playerCount,
            ),
          ),
          Container(
            width: 1,
            height: 40,
            color: AppColors.lightBeige.withOpacity(0.3),
          ),
          Expanded(
            child: _buildInfoItem(
              Icons.timer,
              'Duration',
              '${widget.gameData.duration} min',
            ),
          ),
          Container(
            width: 1,
            height: 40,
            color: AppColors.lightBeige.withOpacity(0.3),
          ),
          Expanded(
            child: _buildInfoItem(
              Icons.trending_up,
              'Difficulty',
              widget.gameData.difficulty,
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildInfoItem(IconData icon, String label, String value) {
    return Column(
      children: [
        Icon(
          icon,
          color: AppColors.lightBeige,
          size: 24,
        ),
        const SizedBox(height: 8),
        Text(
          label,
          style: Theme.of(context).textTheme.bodySmall?.copyWith(
            color: AppColors.lightBeige.withOpacity(0.7),
            fontSize: 12,
          ),
        ),
        const SizedBox(height: 4),
        Text(
          value,
          style: Theme.of(context).textTheme.bodyMedium?.copyWith(
            color: AppColors.lightBeige,
            fontWeight: FontWeight.w600,
          ),
          textAlign: TextAlign.center,
        ),
      ],
    );
  }

  Widget _buildGameDescription() {
    return _buildSection(
      title: 'Description',
      icon: Icons.description,
      child: Text(
        widget.gameData.description,
        style: Theme.of(context).textTheme.bodyLarge?.copyWith(
          height: 1.5,
          color: AppColors.lightBeige.withOpacity(0.9),
        ),
      ),
    );
  }

  Widget _buildRulesSection() {
    return _buildSection(
      title: 'Rules',
      icon: Icons.rule,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: widget.gameData.rules.map((rule) {
          final index = widget.gameData.rules.indexOf(rule) + 1;
          return Padding(
            padding: const EdgeInsets.only(bottom: 12),
            child: Row(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Container(
                  width: 24,
                  height: 24,
                  decoration: BoxDecoration(
                    color: AppColors.primary.withOpacity(0.2),
                    borderRadius: BorderRadius.circular(12),
                    border: Border.all(
                      color: AppColors.primary.withOpacity(0.3),
                    ),
                  ),
                  child: Center(
                    child: Text(
                      '$index',
                      style: Theme.of(context).textTheme.bodySmall?.copyWith(
                        color: AppColors.primary,
                        fontWeight: FontWeight.w600,
                      ),
                    ),
                  ),
                ),
                const SizedBox(width: 12),
                Expanded(
                  child: Text(
                    rule,
                    style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                      height: 1.4,
                      color: AppColors.lightBeige.withOpacity(0.9),
                    ),
                  ),
                ),
              ],
            ),
          );
        }).toList(),
      ),
    );
  }

  Widget _buildInstructionsSection() {
    return _buildSection(
      title: 'How to Play',
      icon: Icons.play_circle_outline,
      child: Text(
        widget.gameData.instructions,
        style: Theme.of(context).textTheme.bodyLarge?.copyWith(
          height: 1.5,
          color: AppColors.lightBeige.withOpacity(0.9),
        ),
      ),
    );
  }

  Widget _buildScoringSection() {
    return _buildSection(
      title: 'Scoring System',
      icon: Icons.emoji_events,
      child: Text(
        widget.gameData.scoringSystem,
        style: Theme.of(context).textTheme.bodyLarge?.copyWith(
          height: 1.5,
          color: AppColors.lightBeige.withOpacity(0.9),
        ),
      ),
    );
  }

  Widget _buildMaterialsSection() {
    return _buildSection(
      title: 'Materials Needed',
      icon: Icons.inventory,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: widget.gameData.materials.map((material) {
          return Padding(
            padding: const EdgeInsets.only(bottom: 8),
            child: Row(
              children: [
                Container(
                  width: 6,
                  height: 6,
                  decoration: BoxDecoration(
                    color: AppColors.primary,
                    borderRadius: BorderRadius.circular(3),
                  ),
                ),
                const SizedBox(width: 12),
                Expanded(
                  child: Text(
                    material,
                    style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                      color: AppColors.lightBeige.withOpacity(0.9),
                    ),
                  ),
                ),
              ],
            ),
          );
        }).toList(),
      ),
    );
  }

  Widget _buildTipsSection() {
    return _buildSection(
      title: 'Pro Tips',
      icon: Icons.lightbulb_outline,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: widget.gameData.tips.map((tip) {
          return Padding(
            padding: const EdgeInsets.only(bottom: 12),
            child: Row(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Icon(
                  Icons.lightbulb,
                  color: AppColors.yellow,
                  size: 16,
                ),
                const SizedBox(width: 12),
                Expanded(
                  child: Text(
                    tip,
                    style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                      color: AppColors.lightBeige.withOpacity(0.9),
                      height: 1.4,
                    ),
                  ),
                ),
              ],
            ),
          );
        }).toList(),
      ),
    );
  }

  Widget _buildSection({
    required String title,
    required IconData icon,
    required Widget child,
  }) {
    return GlassContainer(
      color: AppColors.lightBeige,
      padding: const EdgeInsets.all(20),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              Icon(
                icon,
                color: AppColors.lightBeige,
                size: 24,
              ),
              const SizedBox(width: 12),
              Text(
                title,
                style: Theme.of(context).textTheme.headlineMedium?.copyWith(
                  fontSize: 18,
                  fontWeight: FontWeight.w600,
                ),
              ),
            ],
          ),
          const SizedBox(height: 16),
          child,
        ],
      ),
    );
  }

  Widget _buildBottomActions() {
    return Padding(
      padding: const EdgeInsets.all(24.0),
      child: Row(
        children: [
          Expanded(
            child: CustomButton(
              text: 'Generate New Game',
              onPressed: _generateNewGame,
              backgroundColor: AppColors.lightBeige.withOpacity(0.2),
              textColor: AppColors.lightBeige,
            ),
          ),
          const SizedBox(width: 16),
          Expanded(
            child: CustomButton(
              text: 'Start Playing!',
              onPressed: () {
                AppUtils.showSnackBar(
                  context,
                  'Have fun with your game! üéâ',
                  backgroundColor: AppColors.primary.withOpacity(0.9),
                  textColor: AppColors.onPrimary,
                );
              },
            ),
          ),
        ],
      ),
    );
  }
}